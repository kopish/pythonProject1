# Примеры вещественных чисел
a = 0.5
b = 3.2

# Примеры вещественных чисел в экспоненциальной записи
c = 3.2e5  # то же самое, что и 3.2 * 10**5
d = 1e-3   # то же самое, что и 1 * 10**(-3)

print(a, b, c, d)

# Построение вещественного значения
# из строки
print(float("0.5"))
# из целого числа
print(float(3))
