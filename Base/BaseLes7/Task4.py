# Повторение множеств
a = [1] * 5
# [1, 1, 1, 1, 1]
print(a)
b = [1, 2] * 5
# [1, 2, 1, 2, 1, 2, 1, 2, 1, 2]
print(b)
c = (1, 2, 5, 7, 15)
print(c * 5)

s = input("Введите фразу: ")
n = int(input("Введите количество повторов: "))
print((s * n))

my_range = list(range(10))
n = int(input("Введите количество повторов: "))
print((my_range * n))
